/**
 * O que é o NestJS?
 * NestJS é um framework para aplicações backend para Node.js, escrito em TypeScript, que impõe uma arquitetura organizada para construir APIs e sistemas escaláveis.
 * 
 * Ideia central do NestJS
 * Você declara o que cada parte é. O Nest cria, conecta e executa tudo por você, ou seja você não:
 * - instancia classes com new
 * - registra rotas manualmente
 * - gerencia ciclo de vida
 * - O Nest faz isso usando:
 *      - decorators
 *      - injeção de dependência
 *      - metadados
 *      - runtime
 * 
 * Focado em arquitetura Modular e escalável
 * Fornece um padrão arquitetural
 * Os recursos do NestJS estão disponíveis através de decorators
 * Injeção de dependência
 * Utiliza o express para gerenciamento de rotas
 * 
 */